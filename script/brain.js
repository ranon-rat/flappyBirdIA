import { mathFuncs, gaussianRand } from "./mathfuncs.js";
export class Brain {
    constructor(layers, activationFuncs) {
        this.weights = [];
        this.biases = [];
        this.activationFunctions = [];
        layers.slice(0, layers.length - 1).map((v, i) => {
            this.activationFunctions = activationFuncs;
            this.weights.push([]);
            this.biases.push([]);
            for (let n = 0; n < v; n++) {
                this.weights[i].push([]);
                for (let c = 0; c < layers[i + 1]; c++) {
                    this.weights[i][n].push(Math.random());
                }
            }
            for (let n = 0; n < layers[i + 1]; n++) {
                this.biases[i].push(Math.random());
            }
        });
    }
    foward(input) {
        let layers = [input];
        for (let l = 0; l < (this.biases).length; l++) {
            let layer = this.biases[l].map((b) => b);
            for (let n = 0; n < this.weights[l].length; n++) {
                for (let c = 0; c < this.weights[l][n].length; c++) {
                    layer[c] += layers[l][n] * this.weights[l][n][c];
                }
            }
            // this proyect is made
            layers.push(layer.map(x => mathFuncs(x, this.activationFunctions[l], false)));
        }
        return layers;
    }
    predict(input) {
        let lays = this.foward(input);
        return lays[lays.length - 1];
    }
    // the loss output is for the agent just keep it in mind
    backprop(layers, loss) {
        let bgrad = [];
        let wgrad = [];
        for (let l = this.biases.length - 1; l > 0; l--) {
            let gradient = this.biases[l].map((_, n) => loss[n] * mathFuncs(layers[l + 1][n], this.activationFunctions[l], true));
            let deltGrad = this.weights[l].map((v, i) => gradient.map((g) => layers[l][i] * g));
            bgrad.push(gradient);
            wgrad.push(deltGrad);
            if (l == 0)
                continue;
            loss = layers[l].map((_, i) => {
                let s = 0;
                loss.map((q, j) => { s += q * this.weights[l][i][j]; });
                return s;
            });
        }
        return [bgrad.reverse(), wgrad.reverse()];
    }
    mutate(bestBias, bestWeight) {
        bestBias.map((v, l) => {
            this.biases[l] = v.map(i => randomValue(i));
            this.weights[l] = bestWeight[l].map((n) => n.map(randomValue));
        });
    }
    update(learningRate, bgrad, wgrad) {
        bgrad.map((v, l) => {
            this.biases[l] = v.map((n, k) => this.biases[l][k] - learningRate * n);
            this.weights[l] = wgrad[l].map((n, k) => n.map((wd, c) => this.weights[l][k][c] - learningRate * wd));
        });
    }
}
function randomValue(x) {
    if (Math.random() < 0.1) {
        return Math.random();
    }
    else if (Math.random() < 0.5) {
        return x + gaussianRand();
    }
    return x;
}
